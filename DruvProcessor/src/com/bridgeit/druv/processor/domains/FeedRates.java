package com.bridgeit.druv.processor.domains;

public class FeedRates {

	private  double freq_x,freq_y,freq_z,feed_rate;
	private int delay_x,delay_y,delay_z;
	public double getFreq_x() {
		return freq_x;
	}
	public void setFreq_x(double freq_x) {
		this.freq_x = freq_x;
	}
	public double getFreq_y() {
		return freq_y;
	}
	public void setFreq_y(double freq_y) {
		this.freq_y = freq_y;
	}
	public double getFreq_z() {
		return freq_z;
	}
	public void setFreq_z(double freq_z) {
		this.freq_z = freq_z;
	}
	public double getFeed_rate() {
		return feed_rate;
	}
	public void setFeed_rate(double feed_rate) {
		this.feed_rate = feed_rate;
	}
	public int getDelay_x() {
		return delay_x;
	}
	public void setDelay_x(int delay_x) {
		this.delay_x = delay_x;
	}
	public int getDelay_y() {
		return delay_y;
	}
	public void setDelay_y(int delay_y) {
		this.delay_y = delay_y;
	}
	public int getDelay_z() {
		return delay_z;
	}
	public void setDelay_z(int delay_z) {
		this.delay_z = delay_z;
	}
	
}
