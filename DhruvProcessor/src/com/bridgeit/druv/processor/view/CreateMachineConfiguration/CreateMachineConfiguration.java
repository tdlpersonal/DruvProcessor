    private boolean ClutchVdfFrameSwitcher(String AxisDriveType) {
        //Clutch //VFD
        if (AxisDriveType.equals("Clutch")) {
            new FeedRateConfigurationclutch().setVisible(true);
            return true;
        }
        if (AxisDriveType.equals("VFD")) {
            new FeedRateConfigurationVdf().setVisible(true);
            return true;
        }
        return false;

    }

    private boolean XYZaxisValidatoronConfiguration(String Idendifier) {

        JFrame f = new JFrame("Error");
        JPanel p = new JPanel();
        JLabel l = null;
        //  CCGlobZAxisDriveType
        if (Idendifier.equals("CCGlobXAxisDriveType")) {
            l = new JLabel("Select X-Axis Drive type");
            p.add(l);
            f.add(p);
            f.setLocation(400, 350);
            f.setSize(800, 100);
            f.show();
            return false;
        }
        if (Idendifier.equals("CCGlobYAxisDriveType")) {
            l = new JLabel("Select Y-Axis Drive type");
            p.add(l);
            f.add(p);
            f.setLocation(400, 350);
            f.setSize(800, 100);
            f.show();
            return false;
        }
        if (Idendifier.equals("CCGlobZAxisDriveType")) {
            l = new JLabel("Select Z-Axis Drive type");
            p.add(l);
            f.add(p);
            f.setLocation(400, 350);
            f.setSize(800, 100);
            f.show();
            return false;
        }

        return false;

    }

